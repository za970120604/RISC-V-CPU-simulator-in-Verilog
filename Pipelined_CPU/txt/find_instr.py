def binary_to_hex(binary_str):
    # Convert binary string to hexadecimal string
    hex_str = hex(int(binary_str, 2))[2:]  # Remove the '0x' prefix
    return hex_str.zfill(8)  # Zero-fill to ensure it is 8 characters long

def find_unique_lines(binary_data):
    # Split the input data into lines
    lines = binary_data.strip().split('\n')
    
    # Use a set to find unique lines
    unique_lines = set(lines)
    
    return unique_lines

def convert_lines_to_hex(unique_lines):
    hex_lines = [binary_to_hex(line) for line in unique_lines]
    return hex_lines

